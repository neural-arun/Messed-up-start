while True:    
    a = float(input("Enter number 1 : "))
    b = float(input("Enter number 2 : "))
    c = input("type of operation you want to do(+,-,*,/):")
    if c == "+" :
        print(a+b)
    elif c== "-" :
        print(a-b)
    elif c=="*":
        print(a*b)
        
    elif c == "/" :
        print(a/b)        
    else:
        print("invalid operation") 
    
    
    
    
    